  <script>
    document.addEventListener("DOMContentLoaded", function() {
      const form = document.getElementById("mailsend");
      const contactInput = document.getElementById("contact");
      const gstinInput = document.getElementById("company_gstin");
      const emailInput = document.getElementById("email");
      const subjectInput = document.getElementById("subject");
      const companyNameInput = document.getElementById("company_name");

      form.addEventListener("submit", function(event) {
        event.preventDefault();

        if (!validateContact(contactInput.value)) {
          showError(contactInput, "Contact number must be 10 digits.");
          return;
        }

        if (!validateGSTIN(gstinInput.value)) {
          showError(gstinInput, "GSTIN must be 15 digits.");
          return;
        }

        if (!validateEmail(emailInput.value)) {
          showError(emailInput, "Email cannot be blank.");
          return;
        }

        if (!validateSubject(subjectInput.value)) {
          showError(subjectInput, "Subject cannot be blank.");
          return;
        }

        if (!validateCompanyName(companyNameInput.value)) {
          showError(companyNameInput, "Company name cannot be blank.");
          return;
        }

        // If all validations pass, you can submit the form here
        form.submit();
      });

      function validateContact(contact) {
        return /^\d{10}$/.test(contact);
      }

      function validateGSTIN(gstin) {
        return /^\d{15}$/.test(gstin);
      }

      function validateEmail(email) {
        return email.trim() !== "";
      }

      function validateSubject(subject) {
        return subject.trim() !== "";
      }

      function validateCompanyName(companyName) {
        return companyName.trim() !== "";
      }

      function showError(input, message) {
        const validateDiv = input.parentElement.querySelector(".validate");
        validateDiv.textContent = message;
        validateDiv.classList.add("error");
      }
    });</script>


