  <script>
    document.addEventListener("DOMContentLoaded", function() {
      const filterItems = document.querySelectorAll("#product-filter-flters li");
      const products = document.querySelectorAll(".products-page");

      // Add click event listener to each filter item
      filterItems.forEach(function(item) {
        item.addEventListener("click", function() {
          const filter = this.getAttribute("data-filter");
          filterItems.forEach(function(item) {
            item.classList.remove("filter-active");
          });
          this.classList.add("filter-active");

          // Call the filter function
          filterProducts(filter);
        });
      });

      function filterProducts(filter) {
        console.log("Filter:", filter);
        products.forEach(function(product) {
          const productType = product.getAttribute("id");
          console.log("Product type:", productType);
          if (filter === "*" || productType === filter) {
            product.style.display = "block";
            console.log(productType, "shown");
          } else if ("#"+productType === filter){
            product.style.display = "block";
            console.log(productType, "shown");
          }
          else {
            product.style.display = "none";
            console.log(productType, "hidden");
          }
        });
      }

    });
  </script>



